// Incrementing
// The increment operator (++) increments numbers.

let number = 10;

console.log("Initial number:", number); // Output: Initial number: 10

number++; // Increment the number

console.log("After increment:", number); // Output: After increment: 11

// Decrementing

// The decrement operator (--) decrements numbers.

let decrementedNumber = 10;

console.log("Initial decremented number:", decrementedNumber); // Output: Initial decremented number: 10

decrementedNumber--; // Decrement the number

console.log("After decrement:", decrementedNumber); // Output: After decrement: 9

// Advance incremenintg and decrementing example

let counter = 0;

console.log("Initial counter:", counter); // Output: Initial counter: 0

counter++; // Increment the counter

console.log("After increment:", counter); // Output: After increment: 1

counter--; // Decrement the counter

console.log("After decrement:", counter); // Output: After decrement: 0

counter++; // Increment the counter again

console.log("After increment (again):", counter); // Output: After increment (again): 1

counter--; // Decrement the counter again

console.log("After decrement (again):", counter); // Output: After decrement (again): 0

counter++; // Increment the counter once more

console.log("After increment (once more):", counter); // Output: After increment (once more): 1

counter--; // Decrement the counter once more

console.log("After decrement (once more):", counter); // Output: After decrement (once more): 0

// Using pre-increment and pre-decrement

let preIncrementNumber = 10;

console.log("Initial pre-increment number:", preIncrementNumber); // Output: Initial pre-increment number: 10

let preIncrementedNumber = ++preIncrementNumber;

console.log("After pre-increment:", preIncrementedNumber); // Output: After pre-increment: 11

let preDecrementNumber = 10;

console.log("Initial pre-decrement number:", preDecrementNumber); // Output: Initial pre-decrement number: 10

let preDecrementedNumber = --preDecrementNumber;

console.log("After pre-decrement:", preDecrementedNumber); // Output: After pre-decrement: 9
